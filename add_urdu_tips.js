async function addUrduTips() {
  try {
    console.log('🚀 Starting to add Urdu health tips...');
    console.log(`📝 Total Urdu tips to add: ${urduTips.length}`);
    
    // Get all existing tips
    const collection = await HealthTipModel.getCollection();
    const existingTips = await collection.find({ isActive: true }).toArray();
    
    console.log(`📊 Found ${existingTips.length} existing tips in database`);
    
    // Strategy: Add Urdu translations to existing tips if possible
    // If there are more Urdu tips than existing tips, add them as new tips
    let updatedCount = 0;
    let addedCount = 0;
    
    // First, try to update existing tips with Urdu translations
    for (let i = 0; i < Math.min(existingTips.length, urduTips.length); i++) {
      const existingTip = existingTips[i];
      const urduTip = urduTips[i];
      
      // Only update if tipUrdu doesn't exist
      if (!existingTip.tipUrdu) {
        await HealthTipModel.updateHealthTip(existingTip._id, {
          tipUrdu: urduTip
        });
        updatedCount++;
        console.log(`✅ Updated tip ${i + 1}/${Math.min(existingTips.length, urduTips.length)} with Urdu translation`);
      }
    }
    
    // If there are more Urdu tips than existing tips, add them as new tips
    if (urduTips.length > existingTips.length) {
      const remainingUrduTips = urduTips.slice(existingTips.length);
      const newTips = remainingUrduTips.map((urduTip, index) => ({
        tip: `Health tip ${existingTips.length + index + 1}`, // Placeholder English text
        tipUrdu: urduTip,
        category: 'general',
        priority: 'medium',
        isActive: true
      }));
      
      if (newTips.length > 0) {
        const result = await HealthTipModel.createMultipleHealthTips(newTips);
        addedCount = result.insertedCount || newTips.length;
        console.log(`✅ Added ${addedCount} new tips with Urdu translations`);
      }
    }
    
    console.log('\n📊 Summary:');
    console.log(`   - Updated existing tips: ${updatedCount}`);
    console.log(`   - Added new tips: ${addedCount}`);
    console.log(`   - Total tips processed: ${updatedCount + addedCount}`);
    console.log('\n✅ Urdu tips addition completed successfully!');
    
    process.exit(0);
  } catch (error) {
    console.error('❌ Error adding Urdu tips:', error);
    process.exit(1);
  }
}

// Run the script
addUrduTips();

